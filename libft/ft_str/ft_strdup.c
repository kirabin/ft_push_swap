#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*res;
	int		i;
	int		len;

	i = 0;
	len = ft_strlen((char *)s1);
	res = malloc(sizeof(char) * (len + 1));
	if (!res)
		return (0);
	while (s1[i])
	{
		res[i] = s1[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
